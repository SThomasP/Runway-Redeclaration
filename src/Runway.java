
public class Runway {
	
	int toda;
	int tora;
	int asda;
	int lda;
	char location;
	
	public int getToda(){
		return toda;
	}
	
	public int getTora(){
		return tora;
	}
	
	public int getAsda(){
		return asda;
	}
	
	public int getLda(){
		return lda;
	}
	
	public char getLocation(){
		return location;
	}
	
	public void setToda(int toda){
		this.toda = toda;
	}
	
	public void setTora(int tora){
		this.tora = tora;
	}
	
	public void setAsda(int asda){
		this.asda = asda;
	}
	public void setLda(int lda){
		this.lda = lda;
	}
	
	public void setLocation(char location){
		this.location = location;
	}
	
	
	
}
